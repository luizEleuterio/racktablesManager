package br.jus.trtsp.model;

public class SwitchPlanilha {

	private Integer ordem;
	private String serie;
	private String tombo;
	private String localizacao;
	private String observacoes;
	private String pg_ct_pa;
	private String contratoTrt;
	private String fornecedor;
	private String modelo;
	private String marca;
	private String nome;
	private Integer modeloId;
	
	
	
	public int getOrdem() {
		return ordem;
	}
	public void setOrdem(Integer ordem) {
		this.ordem = ordem;
	}
	public String getSerie() {
		return serie;
	}
	public void setSerie(String serie) {
		this.serie = serie;
	}
	public String getTombo() {
		return tombo;
	}
	public void setTombo(String tombo) {
		this.tombo = tombo;
	}
	public String getLocalizacao() {
		return localizacao;
	}
	public void setLocalizacao(String localizacao) {
		this.localizacao = localizacao;
	}
	public String getObservacoes() {
		return observacoes;
	}
	public void setObservacoes(String observacoes) {
		this.observacoes = observacoes;
	}
	public String getPg_ct_pa() {
		return pg_ct_pa;
	}
	public void setPg_ct_pa(String pg_ct_pa) {
		this.pg_ct_pa = pg_ct_pa;
	}
	public String getContratoTrt() {
		return contratoTrt;
	}
	public void setContratoTrt(String contratoTrt) {
		this.contratoTrt = contratoTrt;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public Integer getModeloId() {
		return modeloId;
	}
	public void setModeloId(Integer modeloId) {
		this.modeloId = modeloId;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getFornecedor() {
		return fornecedor;
	}
	public void setFornecedor(String fornecedor) {
		this.fornecedor = fornecedor;
	}
	
	
	
	
}
